export function monthShort(locale: Locale, month: number): string {
  const arr = MONTHS_SHORT[locale]
  return arr[month - 1] ?? String(month)
}

export function azimuthLabel(deg: number, locale: Locale): string {
  const mapUk: Record<number, string> = {
    0: 'Південь',
    45: 'Південний схід',
    90: 'Схід',
    135: 'Північний схід',
    180: 'Північ',
    225: 'Північний захід',
    270: 'Захід',
    315: 'Південний захід',
  }
  const mapEn: Record<number, string> = {
    0: 'South',
    45: 'Southeast',
    90: 'East',
    135: 'Northeast',
    180: 'North',
    225: 'Northwest',
    270: 'West',
    315: 'Southwest',
  }
  return locale === 'uk' ? mapUk[deg] ?? `${deg}°` : mapEn[deg] ?? `${deg}°`
}
